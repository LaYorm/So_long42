#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*tab;
	size_t			size_s;
	unsigned int	i;

	size_s = ft_strlen(s);
	i = 0;
	tab = (char *)malloc(sizeof(char) * (size_s + 1));
	if (!tab)
		return (NULL);
	while (s[i])
	{
		tab[i] = f(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
